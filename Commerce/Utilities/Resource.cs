namespace Lomo.Commerce.Utilities
{
    /// <summary>
    /// Represents resources accessible via the Lomo Commerce API.
    /// </summary>
    public enum Resource
    {
        /// <summary>
        /// Indicates the Cards resource.
        /// </summary>
        Cards,

        /// <summary>
        /// Indicates the claimed deals resource.
        /// </summary>
        ClaimedDeals,

        /// <summary>
        /// Indicates the deals resource.
        /// </summary>
        Deals,

        /// <summary>
        /// Indicates the merchants resource.
        /// </summary>
        Merchants,

        /// <summary>
        /// Indicates the redeemed deals resource.
        /// </summary>
        RedeemedDeals,

        /// <summary>
        /// Indicates the redemption history resource.
        /// </summary>
        RedemptionHistory
    }
}